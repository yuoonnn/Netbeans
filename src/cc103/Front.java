    public String spaces(int x){
        String spacing = "";
        if (x == 5){
            spacing = "\n";
        }else if (x == 4){
            spacing = "\n\n";
        }else if (x == 3){
            spacing = "\n\n\n";
        }else if (x == 2){
            spacing = "\n\n\n\n";
        }else if (x == 1){
            spacing = "\n\n\n\n\n";
        }else{
            spacing = "\n\n\t*NO ADD-ONS SELECTED*\n\n\n\n";
        }
        return spacing;
    }
