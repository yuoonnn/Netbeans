    private void jToggleButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton2ActionPerformed
        // TODO add your handling code here:
        String addOns = "";
        String size = "";
        int price = 0;
        
        if (smallBtn.isSelected() == true ){
            size = smallBtn.getText();
            price = Integer.parseInt(small.getText());
        }
        if (mediumBtn.isSelected() == true ){
            size = mediumBtn.getText();
            price = Integer.parseInt(medium.getText());
        }
        if (largeBtn.isSelected() == true ){
            size = largeBtn.getText();
            price = Integer.parseInt(large.getText());
        }
        if (famBtn.isSelected() == true ){
            size = famBtn.getText();
            price = Integer.parseInt(famSize.getText());
        }
        if (onionCheck.isSelected() == true){
            addOns += onionCheck.getText()+"\n\n";
            price += 10.00;
        }
        if (pepperCheck.isSelected() == true){
            addOns += pepperCheck.getText()+"\n\n";
            price += 10.00;
        }
        if (blackCheck.isSelected() == true){
            addOns += blackCheck.getText()+"\n\n";
            price += 10.00;
        }
        if (greenCheck.isSelected() == true){
            addOns += greenCheck.getText()+"\n\n"; 
            price += 10.00;
        }
        if (cheeseCheck.isSelected() == true){
            addOns += cheeseCheck.getText()+"\n\n";
            price += 15.00;
        }
        double amount = Double.parseDouble(amountTxt.getText());
        double change = amount - price;
        
        if (change < 0){
            receipt.setText("\nAMOUNT PAID : "+amount+ "\n\nTOTAL PRICE : "+ price+ "\n\nCHANGE : "+change+"\n\n\t***INVALID ORDER***");
        }else {
            receipt.setText("\nAMOUNT PAID : "+amount+ "\n\nTOTAL PRICE : "+ price+ "\n\nCHANGE : "+change);
        }
        String variant = "";
        
        if (hawaiianRad.isSelected()){
            variant = hawaiianRad.getText();
        }else if (hamcheeseRad.isSelected()){
            variant = hamcheeseRad.getText();
        }else if (peperoniRad.isSelected()){
            variant = peperoniRad.getText();
        }
        
        
        try {
            PreparedStatement pst = con.prepareStatement("INSERT INTO orders(variant, size, price, `add ons`)VALUES(?, ?, ?, ?)");
            pst.setString(1,variant);
            pst.setString(2,size);
            pst.setInt(3, (int) price);
            pst.setString(4, addOns.equals("") ? "no" : "yes");
            
            int rowsInserted = pst.executeUpdate();
            
            if (rowsInserted > 0){
                JOptionPane.showMessageDialog(this,"Record Added Successfully!");
                
            }else{JOptionPane.showMessageDialog(this,"Record Insertion Failed!");
            
            }
        } catch (SQLException ex) {
            Logger.getLogger(Front.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jToggleButton2ActionPerformed

